package it.finsoft.oldfashionpound;

public class Pound {
	
	private int p;
	private int s;
	private int d;
	private int r;
	public Pound() {
		super();
	}
	public Pound(int p, int s, int d,int r) {
		super();
		this.p = p;
		this.s = s;
		this.d = d;
		this.r = r;
	}
	public int getP() {
		return p;
	}
	public void setP(int p) {
		this.p = p;
	}
	public int getS() {
		return s;
	}
	public void setS(int s) {
		this.s = s;
	}
	public int getD() {
		return d;
	}
	public void setD(int d) {
		this.d = d;
	}
	public int getR() {
		return r;
	}
	public void setR(int r) {
		this.r = r;
	}
	
	

}
